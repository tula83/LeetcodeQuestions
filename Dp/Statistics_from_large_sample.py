        freq,mode=-9999,0

        for i in range(len(count)):

            if count[i] >=0:

                if freq < count[i]:
                    freq=count[i]
                    mode=i

                for j in range(count[i]):
                    sample.append(i)

       
        

        ans=[]
        n=len(sample)

        ans.append(min(sample))

        ans.append(max(sample))

        ans.append(sum(sample)/n)
        index=n//2


        if n%2:
            ans.append(sample[index])
        else:
            avg=(sample[index]+sample[index-1])/2
            ans.append(avg)
        
        ans.append(mode)

        return ans
        
        
   

